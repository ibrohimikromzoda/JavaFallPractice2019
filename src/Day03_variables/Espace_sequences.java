package Day03_variables;

public class Espace_sequences {
	
	/*
	 /n  or /r : newline
	  /t starts from the middle 
	   /n/n/n adjust 3 line spaces
	    /t : Horizontal line starts from the middle
	     \\ : prints a back slash
	     \' : prints single quote
	     \" : prints double quote
	     
	     \: backslash
	     /:front slash
	     
	 */
	
	
	
	public static void main(String[] args) {
		
		System.out.println("Hello Cybertek\nBatch12 students");
		
		
		System.out.print("Tomorrow is off \n"); // /n or /r starts from newline
		System.out.print("True\n");
		System.out.print("False\n\n\n");  // : /n/n/n adjust 3 line spaces
		
		
		System.out.println("\tCkass starts tomorrow"); // Horizontal line starts from the middle
		
		System.out.println("\\Ibrohim");
		System.out.println("\' Java");
		
		
		System.out.println("Book is called \"Game of Thrones\" ");
	}

}
